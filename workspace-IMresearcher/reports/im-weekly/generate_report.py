def add_heading_custom(text, level=1):
    h = doc.add_heading(text, level=level)
    for run in h.runs:
        run.font.name = '华文楷体'
        run.element.rPr.rFonts.set(qn('w:eastAsia'), '华文楷体')
    return h

def add_para(text, bold=False, italic=False, space_after=6):
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.font.name = '华文楷体'
    run.element.rPr.rFonts.set(qn('w:eastAsia'), '华文楷体')
    run.bold = bold
    run.italic = italic
    run.font.size = Pt(12)
    pf = p.paragraph_format
    pf.space_after = Pt(space_after)
    pf.space_before = Pt(2)
    return p

def add_bullet(text, level=0, space_after=4):
    p = doc.add_paragraph(style='List Bullet')
    run = p.add_run(text)
    run.font.name = '华文楷体'
    run.element.rPr.rFonts.set(qn('w:eastAsia'), '华文楷体')
    run.font.size = Pt(11)
    pf = p.paragraph_format
    pf.space_after = Pt(space_after)
    pf.space_before = Pt(1)
    if level > 0:
        p.paragraph_format.left_indent = Cm(level * 1.27)
    return p

